# 3. 逐个上传
for i, doc in enumerate(documents, 1):
    try:
        resp = requests.post(f'{BASE}/kb/documents', json=doc, headers=headers)
        result = resp.json()
        if result['code'] == 200:
            print(f'[OK] 文档{i} "{doc["title"]}" 上传成功')
        else:
            print(f'[FAIL] 文档{i} "{doc["title"]}" - {result.get("message")}')
    except Exception as e:
        print(f'[ERROR] 文档{i} "{doc["title"]}" - {e}')

# 4. 验证结果
resp = requests.get(f'{BASE}/kb/documents', headers=headers)
